public class latihan11OperatorOperasiPerbandingan {

    public static void main(String[] args) {
//OPERASI PERBANDINGAN
        int a = 100;
        int b = 100;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

    }

}
