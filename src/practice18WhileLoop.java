public class practice18WhileLoop {
    //While loop is simple loop without init statement and post statement
    //If only want to get looping with only condition then while is very good option to go for it
    public static void main(String[] args) {
        var counter = 1;

        while (counter <= 10){
            System.out.println("Looping " + counter);

            counter++;
        }
    }
}
